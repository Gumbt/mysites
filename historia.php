<p>
Fala galera!! Uma breve história de como o clan foi criado. O PSICOWAR foi criado meados de 2014. Nosso foco era guerra e nosso objetivo principal era chegar ao level 10. Amadurecemos como clan, nos esforçamos muito para essa conquista onde muitos foram ficando pelo caminho nessa jornada. A partir dessa conquista (level 10), incentivada pelo nosso saudoso ex-membro Tyrion(Emerson Bolsonaro),criador do PSICO REBORN, decidimos mudar nosso foco para PUSH em dezembro de 2016. Foi uma mudança brusca onde muitos não se adaptaram, foi uma carnificina, onde sobreviveram uns 22 membros apenas (sim, alguns resistem até hoje divididos nos 5 clãs). Não havia mais sentido focar somente em guerra se o máximo de benefícios do clan a gente já tinha conquistado. Seguindo no PUSH, nosso objetivo inicial foi estar entre os tops 200 br. Em seguida: top 50,Top 20,Top 10,Top 5 e agora estamos na briga pelo top 3 BR.
O Clan PSICO REBORN foi criado inicialmente a alguns meses atrás, apenas como uma diversão de vilas secundarias de membros e ex-membros PSICOWAR. Um certo tempo após a retirada de TYRION do Clash, decidimos desativa-la.
O sucesso no PUSH, o destaque no ranking BR, a falta de vagas, a vontade que tínhamos de manter ótimos membros, mas que não estavam no PUSH intenso somado ao frenético interesse das pessoas em querer fazer parte do clan PSICOWAR nos forçou a reativar o clan PSICO REBORN! Era uma necessidade.
</p>
<p>
O sucesso do Psico reborn foi grande! Com a excelente condução do nosso amigo MAXIMUS o Clan se mantém sempre entre os top 10/20 Br.
Um novo clan foi formado,DINASTIA CLÃ, gentilmente cedido pelo nosso amigo JAMES(Psico Reborn) e inicialmente seria para abrigar quem está farmando forte e curte guerra. A faixa pensada inicialmente foi de 3200 a 4500 troféus.
Nosso amigo Dr GUERRA, um dos fundadores do PSICOWAR,está na missão de conduzir o Clan. Ou seja, se você é da nossa família, curte a galera e curte nossa filosofia, sempre terá lugar para você no clan, só dependerá da faixa de troféus que você está curtindo no momento.
</p>
<p>
Como o PSICO REBORN está com foco em guerra e DINASTIA CLÃ está com foco em farm, um quarto clan foi reativado,LIGA DE METAL, com o objetivo de dar suporte ao PSICOWAR no push,gentilmente cedido por Perigos(ex-psicowar/Psico reborn) que está na missão de liderar o Clan rumo ao top 10. 
A família Psicowar foi crescendo e agregando ótimos membros pelo caminho, fazendo sempre novos amigos e parceiros. Agora, o foco do PSICOWAR será ajudar mais um Clan que agora faz parte da família. CAÇADORES. Nossa missão será fazer dos CACADORES, top 5 Br, lugar que lhe pertence por direito!
</p>
<p>
A partir de agora, os 5 clãs são uma coisa só, uma família, uma família com 250 vagas. O Foco do PSICOWAR é bater 6000 troféus e o mínimo de permanência são 5600 troféus. O objetivo do PSICO REBORN é bater 5500 troféus e o mínimo de permanência são 4500 troféus com foco maior em GUERRA. DINASTIA CLÃ o mínimo são 3200, com o objetivo maior de farmar, jogar sem o estresse do push. LIGA DE METAL, Clan de push com mínimo de 4200 e que dá suporte ao PSICOWAR. CACADORES será o mais novo  e audacioso projeto! TOP 5 br
Ou seja, temos 250 vagas para quem quer desempenho em alto nível ou para quem está farmando. Se você faz parte da família, basta escolher o Clan que se encaixe com seu perfil e tempo disponível!!
</p>
